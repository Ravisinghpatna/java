package Practice;

public class AreaOfSquare {
	
	
	
	public static int add(int a)
	{
		int area=a*a;
		return area;
	}

	public static void main(String[] args) {
		
		int c=add(10);
		System.out.println(add(10));
		System.out.println("The area of a square of side 10 is "+c);
		
	}

}
