package Santosh;

public class MethodEvenOdd {
	
	public static void find(int a)
	{
	if(a%2==0)
		{
		System.out.println("It is a Even number ");
		}
	else
	{
		System.out.println("It is a Odd Number");
	}
	}

	public static void main(String[] args) {
	
		find(12670120);
	}

}
